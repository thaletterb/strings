// My Functions
int my_strlen(char* my_string);			// returns the length of my_string
void print_my_string(char *my_string);	// print each element of my_string
char* my_strcpy(char *my_string);		// copies my_string into new variable and returns
char* my_reverse_string(char *my_string);// reverses my_string and returns

